    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        String senha = new String(txtSenha.getPassword());
        Usuario u = new Usuario();

        u.setNome(txtNome.getText());
        u.setEmail(txtEmail.getText());
        u.setSenha(senha);

        boolean res = dao.inserir(u);

        if (res) {
            JOptionPane.showMessageDialog(this, "CADASTRADO COM SUCESSO !!");
        }else{
            JOptionPane.showMessageDialog(this, "ERRO AO TENTAR CADASTRAR USU√ÅRIO !!");
        }
        
        txtNome.setText("");
        txtEmail.setText("");
        txtSenha.setText("");

    }//GEN-LAST:event_btnCadastrarActionPerformed

    private void btnLogarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLogarActionPerformed
        String user = txtEmail2.getText();
        String pass = new String(txtSenha2.getPassword());

        Usuario u = dao.validarUsuario(user, pass);
        if (u != null) {
            JOptionPane.showMessageDialog(this, "SEJA BEM-VINDO, " + u.getNome() + " !!");
            setVisible(false);
        } else {
            JOptionPane.showMessageDialog(this, "ERRO AO TENTAR LOGAR !!");
        }
    }//GEN-LAST:event_btnLogarActionPerformed

